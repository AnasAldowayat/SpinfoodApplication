package main;

/**
 * This enum represents different age ranges.
 */
public enum AgeRange {
    UNDER_18, // Represents people under the age of 18
    BETWEEN_18_AND_25, // Represents people between the ages of 18 and 25
    BETWEEN_26_AND_35, // Represents people between the ages of 26 and 35
    BETWEEN_36_AND_45, // Represents people between the ages of 36 and 45
    BETWEEN_46_AND_55, // Represents people between the ages of 46 and 55
    ABOVE_55 // Represents people over the age of 55
}
