package main;

/**
 * An enumeration representing the kitchen status of a participant.
 *
 * The possible values are:
 * - yes: the participant has access to a kitchen and can cook
 * - no: the participant does not have access to a kitchen and cannot cook
 * - maybe: the participant is unsure if they have access to a kitchen or not
 */
public enum KitchenStatus {
    yes,
    no,
    maybe
}
